# Calcule a média ponderada de um aluno.
# Considere para isso três notas com pesos 3 (prova A), 5 (prova B) e 2 (trabalhos

nota1 = float(input("Digite a primeira nota: "))
nota2 = float(input("Digite a segunda nota: "))
nota3 = float(input("Digite a terceira nota:" ))

soma = ((nota1 * 4) + (nota2 * 4) + (nota3 * 2))
print(soma)